const articleModal = document.getElementById('articleModal');
const articleImage = document.getElementById('articleImage');
const articleLabel = document.getElementById('articleLabel');
const articleTitle = document.getElementById('articleTitle');
const articleIntro = document.getElementById('articleIntro');
const articlePoints = document.getElementById('articlePoints');

function openArticleModal(key) {
  const data = (articleData[currentLang] || articleData.sl)[key];
  if (!data) return;
  articleImage.src = data.image;
  articleImage.alt = data.title;
  articleLabel.textContent = data.label;
  articleTitle.textContent = data.title;
  articleIntro.textContent = data.intro;
  articlePoints.innerHTML = data.points.map((point) => `<li>${point}</li>`).join('');
  articleModal.classList.add('open');
  articleModal.setAttribute('aria-hidden', 'false');
  document.body.classList.add('menu-open');
}

function closeArticleModal() {
  articleModal.classList.remove('open');
  articleModal.setAttribute('aria-hidden', 'true');
  document.body.classList.remove('menu-open');
}

document.querySelectorAll('.insight-card').forEach((card) => {
  card.addEventListener('click', () => openArticleModal(card.dataset.article));
});

document.querySelectorAll('[data-close-article]').forEach((item) => {
  item.addEventListener('click', closeArticleModal);
});

const revealTargets = document.querySelectorAll(
  ".intro-card, .menu-board, .insight-card, .visit-image, .visit-panel, .reviews-copy, .reviews-panel, .reserve-box"
);

revealTargets.forEach((el) => el.classList.add("reveal"));

const observer = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("visible");
        observer.unobserve(entry.target);
      }
    });
  },
  { threshold: 0.13 }
);

revealTargets.forEach((el) => observer.observe(el));

window.addEventListener("keydown", (event) => {
  if (event.key === "Escape" && articleModal.classList.contains("open")) {
    closeArticleModal();
    return;
  }
  if (event.key === "Escape" && dishModal.classList.contains("open")) {
    closeDishModal();
    return;
  }
  if (event.key === "Escape" && nav.classList.contains("open")) {
    nav.classList.remove("open");
    document.body.classList.remove("menu-open");
    navToggle.setAttribute("aria-expanded", "false");
  }
});
