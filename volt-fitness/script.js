const modal = document.getElementById("programModal");
const modalLabel = document.getElementById("modalLabel");
const modalTitle = document.getElementById("modalTitle");
const modalIntro = document.getElementById("modalIntro");
const modalFor = document.getElementById("modalFor");
const modalDuration = document.getElementById("modalDuration");
const modalLevel = document.getElementById("modalLevel");
const modalPoints = document.getElementById("modalPoints");

function openProgramModal(programKey) {
  const data = (programDetails[currentLang] || programDetails.sl)[programKey];
  if (!data) return;

  modalLabel.textContent = data.label;
  modalTitle.textContent = data.title;
  modalIntro.textContent = data.intro;
  modalFor.textContent = data.forText;
  modalDuration.textContent = data.duration;
  modalLevel.textContent = data.level;
  modalPoints.innerHTML = data.points.map((point) => `<li>${point}</li>`).join("");

  modal.classList.add("open");
  modal.setAttribute("aria-hidden", "false");
  document.body.classList.add("menu-open");
}

function closeProgramModal() {
  modal.classList.remove("open");
  modal.setAttribute("aria-hidden", "true");
  document.body.classList.remove("menu-open");
}

document.querySelectorAll(".learn-more").forEach((button) => {
  button.addEventListener("click", (event) => {
    event.stopPropagation();
    openProgramModal(button.dataset.program);
  });
});

document.querySelectorAll("[data-close-modal]").forEach((item) => {
  item.addEventListener("click", closeProgramModal);
});

const revealTargets = document.querySelectorAll(
  ".intro-card, .program-card, .performance-copy, .performance-bars, .schedule-list, .price-card, .trainer-image, .trainer-copy, .contact-card, .contact-details"
);

revealTargets.forEach((el) => el.classList.add("reveal"));

const observer = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("visible");
        observer.unobserve(entry.target);
      }
    });
  },
  { threshold: 0.13 }
);

revealTargets.forEach((el) => observer.observe(el));

window.addEventListener("keydown", (event) => {
  if (event.key === "Escape" && modal.classList.contains("open")) {
    closeProgramModal();
    return;
  }
  if (event.key === "Escape" && nav.classList.contains("open")) {
    nav.classList.remove("open");
    document.body.classList.remove("menu-open");
    navToggle.setAttribute("aria-expanded", "false");
  }
});
